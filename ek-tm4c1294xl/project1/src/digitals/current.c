// проверяет типы счетчиков без получасовых графиков
bool    IsSpecCurrent(uchar  i)
{
  if ((i == 12) ||
      (i == 16) ||
      (i == 17) ||
      (i == 18) ||
      (i == 19) ||
      (i == 23)) return(1);

  return(0);
}
